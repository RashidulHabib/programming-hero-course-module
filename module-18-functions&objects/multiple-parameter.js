// function for two number addition
function addNumbers(number1, number2) {
  // console.log(number1, number2);
  var total = number1 + number2;
  return total;
}

var result = addNumbers(25, 25);
console.log("Result from two number addition: ", result);

// function for two number multiplication
function subTwoNumber(num1, num2) {
  var subResult = num1 * num2;
  return subResult;
}

var subResultTwoNumber = subTwoNumber(52, 28);
console.log("Result from two number multiplication: ", subResultTwoNumber);

// function for two number division
function twoNumberDivision(number3, number4) {
  var divResesult = number3 / number4;
  return divResesult;
}

var firstNumber = 85;
var secondNumber = 5;

var divisionResult = twoNumberDivision(firstNumber, secondNumber);
console.log("Result from two number division: ", divisionResult);
